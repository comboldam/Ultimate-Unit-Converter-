export let currencies: Currency[] = [];

export function buildCurrenciesFromRates(rates: Record<string, number>): Currency[] {
  const currencyCodes = Object.keys(rates);

  return currencyCodes.map(code => {
    const metadata = currencyMetadata[code];
    return {
      code,
      name: metadata?.name || code,
      symbol: metadata?.symbol || code,
      flag: metadata?.flag || 'ğŸŒ',
      country: metadata?.country || code,
    };
  });
}

export function updateCurrencies(rates: Record<string, number>) {
  currencies = buildCurrenciesFromRates(rates);
}

export function getCurrencyByCode(code: string): Currency | undefined {
  return currencies.find(c => c.code === code);
}

export function formatCurrencyValue(value: number, precision: number = 6): string {
  if (Math.abs(value) < 0.000001) {
    return value.toExponential(6);
  }
  return value.toFixed(precision);
}
